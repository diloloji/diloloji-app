function normalizeKey(verb: string): string {
  return verb.trim().toLowerCase();
}

type Lang = 'fr' | 'es';

/**
 * Seçili dile göre fiilin Türkçe anlamını döndürür.
 */
export function getTranslation(verb: string, lang: Lang): string | null {
  const key = normalizeKey(verb);
  if (lang === 'es') {
    return spanishTranslations[key] ?? null;
  }
  return frenchTranslations[key] ?? null;
}

/**
 * Öğrenme/Quiz UI için: anlam varsa döndür, yoksa "Anlamı aranıyor...".
 */
export function getTranslationOrPlaceholder(verb: string, lang: Lang): string {
  const t = getTranslation(verb, lang);
  if (t) return t;
  return 'Anlamı aranıyor...';
}
